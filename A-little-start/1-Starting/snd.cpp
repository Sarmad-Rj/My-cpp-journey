#include <iostream>
using namespace std;
int main()
{

	int a, b, sum, sub, div, mul, mod;
	cout<<"Enter Your First Number ";
	cin>>a;
	cout<<"Enter Your Second Number ";
	cin>>b;
	sum = a + b;
	sub = a - b;
	div = a / b;
	mul = a * b;
	mod = a % b;
	cout<<"Sum:"<< sum<<endl; 
	cout<<"Sub is:"<< sub<<endl; 
	cout<<"Division is:"<< div<<endl; 
	cout<<"Multiplying is:"<< mul<<endl; 
	cout<<"Mod is:"<< mod<<endl; 
	

}
